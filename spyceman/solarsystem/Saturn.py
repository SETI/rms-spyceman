"""\
Support for Saturn-specific kernels.

The following attributes are defined:

ALL_MOONS       the set of all IDs for Saturn's moons, including aliases.
CLASSICAL       the set of IDs of the 'classical' satellites, Mimas to Phoebe.
SMALL_INNER     the set of IDs of the small inner moons.
REGULAR         the set of IDs of the regular satellites.
IRREGULAR       the set of IDs of the irregular satellites, including their aliases.
UNNAMED         the set of IDs of moons that are not yet officially named.
ID              the NAIF ID of Saturn.
SYSTEM          the set of IDs of the planet and all inner or classical moons.
ALL_IDS         the set of IDs of the planet and all moons.
BARYCENTER      the NAIF ID of the Saturn system barycenter.
spk()           a Kernel object derived from one or more Saturn SPK files.

In addition, the NAIF body ID of every moon can be reference by name:

MIMAS           601, the body ID of Mimas.
ENCELADUS       602, the body ID of Enceladus.
S2004_S01       65093, the body ID of S/2004 S 1.
"""

